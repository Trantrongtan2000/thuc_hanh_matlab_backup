n=-20:20;
num = [0.3 0.2 -0.3];
den = [1 0.9 -0.9];
h1=impz(num,den,n);
num1=[2 -0.5 0 0.5];
den1=[1 -0.1 0 0 ];
h2=impz(num1,den1,n);
num2=[0.3 0 0.2];
den2=[1 0 0.1];
h3=impz(num2,den2,n);
h4=h1;
x=(-0.8).^n.*(n>=0);
h1234=conv(h4,conv(h1,(h2+h3)));
y=conv(x,h1234);
subplot(311); stem(x); title('x(n)');grid on;
subplot(312); stem(h1234); title('h(n)');grid on;
subplot(313); stem(y); title('y(n)');grid on;